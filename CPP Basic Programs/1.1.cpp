#include<iostream>
using namespace std;
int main()
{
	int a;
	cout<<"size of int"<<sizeof(a)<<endl;
	float b;
	cout<<"size of float"<<sizeof(b)<<endl;
	char c;
	cout<<"size of char"<<sizeof(c)<<endl;
	bool d;
	cout<<"size of bool"<<sizeof(d)<<endl;
	short int x;
	cout<<"size of x"<<sizeof(x)<<endl;
	long int y;
	cout<<"size of y"<<sizeof(y)<<endl;
	return 0;
}